import "server-only";
import { prisma } from "@/lib/prisma";

/* นโยบายความเป็นส่วนตัว (PDPA) — template ภาษาไทยกลาง ๆ ที่เติมชื่อ/ช่องทางติดต่อของร้าน
   ร้านแก้ "ชื่อผู้ควบคุมข้อมูล" + "ช่องทางติดต่อ" ได้ที่ /admin/settings (ตัวเนื้อห้ามขาด — กันนโยบายไม่ครบ)
   โมเดล 1 deploy = 1 ร้าน → หน้า /privacy ดึงร้านเดียวด้วย findFirst */

export type PrivacyInfo = {
  /** ชื่อผู้ควบคุมข้อมูลที่แสดงในนโยบาย (legalName ถ้ามี, ไม่งั้นใช้ชื่อร้าน) */
  controller: string;
  /** ช่องทางติดต่อเรื่องข้อมูลส่วนบุคคล (อาจว่างถ้าร้านยังไม่กรอก) */
  contact: string;
};

/** ดึงข้อมูลผู้ควบคุมข้อมูลของร้าน (หน้า public — ไม่มี session) */
export async function getPrivacyInfo(): Promise<PrivacyInfo> {
  const s = await prisma.store.findFirst({
    select: { name: true, legalName: true, privacyContact: true },
  });
  const controller = (s?.legalName || s?.name || "ร้านค้า").trim();
  return { controller, contact: (s?.privacyContact ?? "").trim() };
}

export type PrivacySection = { heading: string; body: string[] };

